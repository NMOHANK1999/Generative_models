class UpSampleConv2D(torch.jit.ScriptModule): #the input channel= nfilters(which is output channels), just the h and w is multiplied
    def __init__(
        self,
        input_channels,
        kernel_size=3,
        n_filters=128,
        upscale_factor=2,
        padding=0,
    ):
        super(UpSampleConv2D, self).__init__()
        self.conv = nn.Conv2d(
            input_channels, n_filters, kernel_size=kernel_size, padding=padding
        )
        self.upscale_factor = upscale_factor

    @torch.jit.script_method
    def forward(self, x):
        ##################################################################
        # TODO 1.1: Implement nearest neighbor upsampling
        # 1. Repeat x channel-wise upscale_factor^2 times
        # 2. Use torch.nn.PixelShuffle to form an output of dimension
        # (batch, channel, height*upscale_factor, width*upscale_factor)
        # 3. Apply convolution and return output
        ##################################################################
        out = torch.repeat_interleave(x, int((self.upscale_factor)**2), dim = 1) # dim =0 is batch dim
        out = F.pixel_shuffle(out, self.upscale_factor)
        out = self.conv(out)
        return out
